#!/usr/bin/env node
/**
 * ensure-registry.mjs
 *
 * Creates src/plugins/registry.ts and api/plugin-routes.ts with empty stubs if
 * they do not already exist, then reinstalls any plugin npm packages listed in
 * plugin-deps.json (which is gitignored so plugin deps stay out of package.json).
 *
 * Run automatically as predev and prebuild so a fresh clone is immediately
 * buildable without having to run install-plugins.mjs first.
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs';
import { spawnSync } from 'child_process';
import { dirname, join, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname          = dirname(fileURLToPath(import.meta.url));
const ROOT               = resolve(__dirname, '..');
const PLUGINS_DIR        = join(ROOT, 'src', 'plugins');
const REGISTRY_FILE      = join(PLUGINS_DIR, 'registry.ts');
const PLUGIN_ROUTES_FILE = join(ROOT, 'api', 'plugin-routes.ts');
const PLUGIN_DEPS_FILE   = join(ROOT, 'plugin-deps.json');

if (!existsSync(REGISTRY_FILE)) {
  mkdirSync(PLUGINS_DIR, { recursive: true });
  writeFileSync(REGISTRY_FILE, `/**
 * AUTO-GENERATED by scripts/install-plugins.mjs — do not edit manually.
 *
 * This file is regenerated every time install-plugins.mjs runs.
 * It imports each installed plugin's default export (a PluginDefinition)
 * and collects them into a single array for the loader to consume.
 *
 * To add a plugin: run \`node scripts/install-plugins.mjs --add <github-url>\`
 * then rebuild and redeploy.
 */

import type { PluginDefinition } from '@/types/plugin';

// ─── Installed Plugin Imports ─────────────────────────────────────────────────
// (no plugins installed)


// ─── Registry ─────────────────────────────────────────────────────────────────
const registeredPlugins: PluginDefinition[] = [
  // (no plugins installed)
];

export default registeredPlugins;
`, 'utf8');
  console.log('i  Created empty src/plugins/registry.ts');
}

if (!existsSync(PLUGIN_ROUTES_FILE)) {
  writeFileSync(PLUGIN_ROUTES_FILE, `// AUTO-GENERATED by scripts/install-plugins.mjs — do not edit manually.
// Re-run \`npm run plugin:install\` to regenerate.
import type { Hono } from 'hono';
import type { Env } from './lib/supabase';

// (no plugins with API routes installed)

export function mountPluginRoutes(_app: Hono<{ Bindings: Env }>): void {
  // (no plugins with API routes installed)
}
`, 'utf8');
  console.log('i  Created empty api/plugin-routes.ts');
}

// ─── Reinstall plugin npm packages (from gitignored plugin-deps.json) ─────────
if (existsSync(PLUGIN_DEPS_FILE)) {
  let deps;
  try { deps = JSON.parse(readFileSync(PLUGIN_DEPS_FILE, 'utf8')); } catch { deps = {}; }

  // Merge all per-plugin deps; last version for a given package wins
  const merged = {};
  for (const pkgMap of Object.values(deps)) {
    for (const [name, version] of Object.entries(pkgMap)) merged[name] = version;
  }
  const pkgs = Object.entries(merged).map(([n, v]) => `${n}@${v}`);

  if (pkgs.length > 0) {
    console.log(`i  Reinstalling ${pkgs.length} plugin package(s) from plugin-deps.json…`);
    const npmCmd = process.platform === 'win32' ? 'npm.cmd' : 'npm';
    // --no-package-lock: resolve fresh from registry so the CMS lockfile doesn't
    // force conservative (minimum-version) resolution for plugin-managed packages.
    const result = spawnSync(npmCmd, ['install', '--no-save', '--legacy-peer-deps', '--no-package-lock', ...pkgs], { cwd: ROOT, stdio: 'inherit' });
    if (result.status !== 0) {
      console.warn('!  Plugin package install failed. Run manually:');
      console.warn(`   npm install --no-save --legacy-peer-deps --no-package-lock ${pkgs.join(' ')}`);
    } else {
      console.log('i  Plugin packages ready.');
    }
  }
}
